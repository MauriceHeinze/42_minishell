char	*ft_strchr(const char *s, int c)
{
	int		i;
	char	chr;

	i = 0;
	chr = c;
	if (chr == 0)
	{
		while (*(s + i) != chr)
		{
			i++;
		}
	}
	else
	{
		while (*(s + i) != chr)
		{
			i++;
			if (*(s + i) == 0)
			{
				return ((void *)0);
			}
		}
	}
	return ((char *)(s + i));
}
