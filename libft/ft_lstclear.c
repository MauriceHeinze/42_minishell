#include "libft.h"

void	ft_lstclear(t_list **lst)
{
	t_list	*ls;

	while (*lst)
	{
		ls = (*lst)->n;
		free(*lst);
		*lst = ls;
	}
	*lst = NULL;
	free(ls);
}
