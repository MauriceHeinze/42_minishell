#include "libft.h"

void	ft_lstadd_back(t_list **lst, t_list *new)
{	
	t_list	*ptr;

	if (!lst || !new)
		return ;
	if (*lst == NULL)
	{
		*lst = new;
		return ;
	}
	ptr = *lst;
	while (ptr->n)
		ptr = ptr->n;
	ptr->n = new;
	return ;
}
